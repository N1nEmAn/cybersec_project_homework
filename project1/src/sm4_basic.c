/* Utility Functions */
uint32_t sm4_rotl(uint32_t x, int n) {
    return (x << n) | (x >> (32 - n));
}

uint32_t sm4_tau(uint32_t a) {
    uint8_t b[4];
    b[0] = sm4_sbox[(a >> 24) & 0xFF];
    b[1] = sm4_sbox[(a >> 16) & 0xFF];
    b[2] = sm4_sbox[(a >> 8) & 0xFF];
    b[3] = sm4_sbox[a & 0xFF];
    return (uint32_t)b[0] << 24 | (uint32_t)b[1] << 16 | (uint32_t)b[2] << 8 | (uint32_t)b[3];
}

uint32_t sm4_l(uint32_t b) {
    return b ^ sm4_rotl(b, 2) ^ sm4_rotl(b, 10) ^ sm4_rotl(b, 18) ^ sm4_rotl(b, 24);
}

uint32_t sm4_l_prime(uint32_t b) {
    return b ^ sm4_rotl(b, 13) ^ sm4_rotl(b, 23);
}

/* Key Schedule */
void sm4_setkey_enc(sm4_ctx_t *ctx, const uint8_t key[SM4_KEY_SIZE]) {
    uint32_t k[4];
    uint32_t mk[4];
    int i;
    
    /* Load key into 32-bit words */
    for (i = 0; i < 4; i++) {
        k[i] = ((uint32_t)key[i * 4] << 24) |
               ((uint32_t)key[i * 4 + 1] << 16) |
               ((uint32_t)key[i * 4 + 2] << 8) |
               ((uint32_t)key[i * 4 + 3]);
    }
    
    /* Initialize with FK */
    for (i = 0; i < 4; i++) {
        mk[i] = k[i] ^ sm4_fk[i];
    }
    
    /* Generate round keys */
    for (i = 0; i < SM4_ROUNDS; i++) {
        ctx->rk[i] = mk[0] ^ sm4_l_prime(sm4_tau(mk[1] ^ mk[2] ^ mk[3] ^ sm4_ck[i]));
        mk[0] = mk[1];
        mk[1] = mk[2];
        mk[2] = mk[3];
        mk[3] = ctx->rk[i];
    }
}

void sm4_setkey_dec(sm4_ctx_t *ctx, const uint8_t key[SM4_KEY_SIZE]) {
    uint32_t temp_rk[SM4_ROUNDS];
    int i;
    
    /* Generate encryption round keys first */
    sm4_setkey_enc(ctx, key);
    
    /* Copy to temp array */
    memcpy(temp_rk, ctx->rk, sizeof(temp_rk));
    
    /* Reverse the order for decryption */
    for (i = 0; i < SM4_ROUNDS; i++) {
        ctx->rk[i] = temp_rk[SM4_ROUNDS - 1 - i];
    }
}

/* Basic SM4 Block Encryption */
void sm4_encrypt_basic(const sm4_ctx_t *ctx, const uint8_t input[SM4_BLOCK_SIZE], uint8_t output[SM4_BLOCK_SIZE]) {
    uint32_t x[4];
    uint32_t temp;
    int i;
    
    /* Load input into 32-bit words */
    for (i = 0; i < 4; i++) {
        x[i] = ((uint32_t)input[i * 4] << 24) |
               ((uint32_t)input[i * 4 + 1] << 16) |
               ((uint32_t)input[i * 4 + 2] << 8) |
               ((uint32_t)input[i * 4 + 3]);
    }
    
    /* 32 rounds of SM4 */
    for (i = 0; i < SM4_ROUNDS; i++) {
        temp = x[1] ^ x[2] ^ x[3] ^ ctx->rk[i];
        temp = sm4_tau(temp);
        temp = sm4_l(temp);
        x[0] ^= temp;
        
        /* Rotate the words */
        temp = x[0];
        x[0] = x[1];
        x[1] = x[2];
        x[2] = x[3];
        x[3] = temp;
    }
    
    /* Reverse final transformation */
    temp = x[0]; x[0] = x[3]; x[3] = temp;
    temp = x[1]; x[1] = x[2]; x[2] = temp;
    
    /* Store output */
    for (i = 0; i < 4; i++) {
        output[i * 4] = (x[i] >> 24) & 0xFF;
        output[i * 4 + 1] = (x[i] >> 16) & 0xFF;
        output[i * 4 + 2] = (x[i] >> 8) & 0xFF;
        output[i * 4 + 3] = x[i] & 0xFF;
    }
}

/* Basic SM4 Block Decryption */
void sm4_decrypt_basic(const sm4_ctx_t *ctx, const uint8_t input[SM4_BLOCK_SIZE], uint8_t output[SM4_BLOCK_SIZE]) {
    /* For SM4, decryption uses the same algorithm with reversed round keys */
    sm4_encrypt_basic(ctx, input, output);
}

/* ECB Mode Implementation */
int sm4_ecb_encrypt(const sm4_ctx_t *ctx, const uint8_t *input, size_t length, uint8_t *output) {
    size_t i;
    
    if (length % SM4_BLOCK_SIZE != 0) {
        return -1; /* Invalid length for ECB mode */
    }
    
    for (i = 0; i < length; i += SM4_BLOCK_SIZE) {
        sm4_encrypt_basic(ctx, input + i, output + i);
    }
    
    return 0;
}

int sm4_ecb_decrypt(const sm4_ctx_t *ctx, const uint8_t *input, size_t length, uint8_t *output) {
    size_t i;
    
    if (length % SM4_BLOCK_SIZE != 0) {
        return -1; /* Invalid length for ECB mode */
    }
    
    for (i = 0; i < length; i += SM4_BLOCK_SIZE) {
        sm4_decrypt_basic(ctx, input + i, output + i);
    }
    
    return 0;
}

/* CBC Mode Implementation */
int sm4_cbc_encrypt(const sm4_ctx_t *ctx, uint8_t iv[SM4_BLOCK_SIZE], 
                    const uint8_t *input, size_t length, uint8_t *output) {
    uint8_t temp[SM4_BLOCK_SIZE];
    size_t i, j;
    
    if (length % SM4_BLOCK_SIZE != 0) {
        return -1; /* Invalid length for CBC mode */
    }
    
    for (i = 0; i < length; i += SM4_BLOCK_SIZE) {
        /* XOR with IV/previous ciphertext */
        for (j = 0; j < SM4_BLOCK_SIZE; j++) {
            temp[j] = input[i + j] ^ iv[j];
        }
        
        /* Encrypt */
        sm4_encrypt_basic(ctx, temp, output + i);
        
        /* Update IV */
        memcpy(iv, output + i, SM4_BLOCK_SIZE);
    }
    
    return 0;
}

int sm4_cbc_decrypt(const sm4_ctx_t *ctx, uint8_t iv[SM4_BLOCK_SIZE], 
                    const uint8_t *input, size_t length, uint8_t *output) {
    uint8_t temp[SM4_BLOCK_SIZE];
    uint8_t next_iv[SM4_BLOCK_SIZE];
    size_t i, j;
    
    if (length % SM4_BLOCK_SIZE != 0) {
        return -1; /* Invalid length for CBC mode */
    }
    
    for (i = 0; i < length; i += SM4_BLOCK_SIZE) {
        /* Save next IV */
        memcpy(next_iv, input + i, SM4_BLOCK_SIZE);
        
        /* Decrypt */
        sm4_decrypt_basic(ctx, input + i, temp);
        
        /* XOR with IV */
        for (j = 0; j < SM4_BLOCK_SIZE; j++) {
            output[i + j] = temp[j] ^ iv[j];
        }
        
        /* Update IV */
        memcpy(iv, next_iv, SM4_BLOCK_SIZE);
    }
    
    return 0;
}

/* PKCS#7 Padding */
size_t sm4_pkcs7_padding_add(uint8_t *data, size_t length, size_t buffer_size) {
    size_t padding = SM4_BLOCK_SIZE - (length % SM4_BLOCK_SIZE);
    size_t i;
    
    if (length + padding > buffer_size) {
        return 0; /* Buffer too small */
    }
    
    for (i = length; i < length + padding; i++) {
        data[i] = (uint8_t)padding;
    }
    
    return length + padding;
}

size_t sm4_pkcs7_padding_remove(const uint8_t *data, size_t length) {
    uint8_t padding;
    size_t i;
    
    if (length == 0 || length % SM4_BLOCK_SIZE != 0) {
        return 0; /* Invalid length */
    }
    
    padding = data[length - 1];
    
    if (padding == 0 || padding > SM4_BLOCK_SIZE) {
        return 0; /* Invalid padding */
    }
    
    /* Verify padding */
    for (i = length - padding; i < length; i++) {
        if (data[i] != padding) {
            return 0; /* Invalid padding */
        }
    }
    
    return length - padding;
}
